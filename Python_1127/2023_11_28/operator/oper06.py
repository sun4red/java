# 복합 대입 연산자 : +=, -=, *=, /=, //=, %=

a = 0                   # 초기값

a += 1                  # a = a + 1
print('a=', a)          # a = 1

a -= 5                  # a = a - 5
print('a=', a)          # a= -4

a *= 2                  # a = a * 2
print('a=', a)          # a= -8

a /= 4                  # a = a / 4
print('a=', a)          # a= -2.0







