# 반복문 : while문
# while  조건식 :
#     조건식이 참인 경우에 실행될 문장

# '사랑해요' 메세지를 10번 출력

i = 1                          # 초기값 
while i <= 10 :                # 조건식
    print(i,'사랑해요')
    # i++                      # 파이썬은 증감 연산자를 지원하지 않는다
    # i = i + 1                #  ++i (x), i++(x)
    i += 1                     # 증감식


