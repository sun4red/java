# 문자열 연산하기 : +, *(반복)

head = 'Python'
tail = ' is fun'
str = head + tail
print(str)                      # Python is fun
print(head + tail)              # Python is fun

s = 'python'
print(s * 2)                    # 2번 반복 : pythonpython
print(s * 3)                    # 3번 반복 : pythonpythonpython

print('='*50)                   # 50번 반복
print('My Program')
print('='*50)                   # 50번 반복

